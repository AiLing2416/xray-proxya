package xray

import "testing"

func TestBuildDNSQueryAndParseAResponse(t *testing.T) {
	query, err := buildDNSQuery("example.com", DNSTypeA)
	if err != nil {
		t.Fatalf("buildDNSQuery() error = %v", err)
	}
	if len(query) < 12 {
		t.Fatalf("query too short: %d", len(query))
	}

	response := []byte{
		0x12, 0x34, 0x81, 0x80, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00,
		0x07, 'e', 'x', 'a', 'm', 'p', 'l', 'e', 0x03, 'c', 'o', 'm', 0x00,
		0x00, 0x01, 0x00, 0x01,
		0xc0, 0x0c,
		0x00, 0x01, 0x00, 0x01,
		0x00, 0x00, 0x00, 0x3c,
		0x00, 0x04,
		0x5d, 0xb8, 0xd8, 0x22,
	}

	answers, err := parseDNSAnswers(response, DNSTypeA)
	if err != nil {
		t.Fatalf("parseDNSAnswers() error = %v", err)
	}
	if len(answers) != 1 || answers[0] != "93.184.216.34" {
		t.Fatalf("answers = %v, want [93.184.216.34]", answers)
	}
}

func TestParseDNSAnswersAAAA(t *testing.T) {
	response := []byte{
		0x12, 0x34, 0x81, 0x80, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00,
		0x07, 'e', 'x', 'a', 'm', 'p', 'l', 'e', 0x03, 'c', 'o', 'm', 0x00,
		0x00, 0x1c, 0x00, 0x01,
		0xc0, 0x0c,
		0x00, 0x1c, 0x00, 0x01,
		0x00, 0x00, 0x00, 0x3c,
		0x00, 0x10,
		0x26, 0x06, 0x28, 0x00, 0x02, 0x20, 0x00, 0x01,
		0x02, 0x48, 0x18, 0x93, 0x25, 0xc8, 0x19, 0x46,
	}

	answers, err := parseDNSAnswers(response, DNSTypeAAAA)
	if err != nil {
		t.Fatalf("parseDNSAnswers() error = %v", err)
	}
	if len(answers) != 1 || answers[0] != "2606:2800:220:1:248:1893:25c8:1946" {
		t.Fatalf("answers = %v, want parsed AAAA", answers)
	}
}
